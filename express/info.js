/*
    - Express
        - Express is a web application framework for Node.js, designed for building web applications and APIs
        - It simplifies the process of creating server-side applications by providing a robust set of features
        - It is built on top of Node.js and provides a higher-level abstraction for handling HTTP requests and responses

    - CRUD : Operacione básicas para manejar datos en una aplicación
        - Create, Read, Update, Delete
        - Create: POST
        - Read: GET
        - Update: PUT/PATCH
        - Delete: DELETE
    
    - API: (Application Programming Interface)
        - Interfaz que permite la comunicación entre diferentes sistemas o aplicaciones
        - Permite que diferentes aplicaciones se comuniquen entre sí
        - Intermediario entre el cliente y el servidor
    
    - REST (Representation State Transfer)
        - Estilo arquitectónico para diseñar servicios web.
        - Se basa en el uso de HTTP y se centra en la transferencia de recursos.
    
    -Restful API
        - API que sigue los principios de REST
        - Utiliza los métodos HTTP (GET, POST, PUT, DELETE) para realizar operaciones CRUD
        - Cada recurso tiene una URL única
        - Los recursos se representan en formatos como JSON o XML
        - Stateless: cada solicitud del cliente al servidor debe contener toda la
                     información necesaria para entender y procesar la solicitud.
        - Cacheable: las respuestas deben ser cacheables para mejorar el rendimiento.
        - Uniform Interface: una interfaz uniforme entre los componentes del sistema
        - Layered System: la arquitectura puede estar compuesta por capas, cada una con su propia responsabilidad.
        - Code on Demand (opcional): permite la ejecución de código en el cliente bajo demanda.
*/      